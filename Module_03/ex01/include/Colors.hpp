#ifndef COLORS_HPP
# define COLORS_HPP

// Reset colors
# define RESET					"\033[0;0m"

// Printf Colors
# define BLACK					"\033[0;30m"
# define RED					"\033[0;31m"
# define GREEN					"\033[0;32m"
# define YELLOW					"\033[0;33m"
# define BLUE					"\033[0;34m"
# define PURPLE					"\033[0;35m"
# define CYAN					"\033[0;36m"
# define WHITE					"\033[0;37m"

// Black on colors
# define BLACK_ON_RED			"\033[30;41m"
# define BLACK_ON_GREEN			"\033[30;42m"
# define BLACK_ON_YELLOW		"\033[30;43m"
# define BLACK_ON_BLUE			"\033[30;44m"
# define BLACK_ON_PURPLE		"\033[30;45m"
# define BLACK_ON_CYAN			"\033[30;46m"
# define BLACK_ON_WHITE			"\033[30;47m"

#endif